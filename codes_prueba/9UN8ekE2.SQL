SELECT d.MOVIE_TITLE AS 'can trade', f.MOVIE_TITLE AS 'for', member.USER_NAME AS 'from' 
FROM (
	SELECT MOVIE_TITLE, MEMBER_ID
	FROM(
		SELECT MOVIE_ID, MEMBER_ID
		FROM (SELECT MOVIE_ID
			FROM list_entry
			WHERE MEMBER_ID = 1
	 		AND STATUS_ID = 1
			) AS a
		INNER JOIN (SELECT MOVIE_ID, MEMBER_ID
			FROM list_entry
			WHERE STATUS_ID = 4
			) AS b
		USING(MOVIE_ID)
		) AS c
		NATURAL JOIN movie
	) AS d	 	
INNER JOIN (
	SELECT MOVIE_TITLE, MEMBER_ID
	FROM (
		SELECT MOVIE_ID, MEMBER_ID
		FROM list_entry
		WHERE STATUS_ID = 1
		) AS e
	INNER JOIN movie USING(MOVIE_ID)
	) AS f
USING(MEMBER_ID)
NATURAL JOIN member