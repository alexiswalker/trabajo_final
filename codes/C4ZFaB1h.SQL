CREATE TABLE Vozilo(
	REGOZNAKA_VOZILA NVARCHAR(9),
	MARKA_VOZILA NVARCHAR(20) NOT NULL,
	MODEL_VOZILA NVARCHAR(20) NOT NULL,
	GOD_PROIZVODNJE NUMERIC(4) NOT NULL,
	CONSTRAINT PK_Vozilo PRIMARY KEY(REGOZNAKA_VOZILA)
	);


CREATE TABLE Instruktor(
	SIFRA_INSTRUKTORA NUMERIC(5),
	IME_INSTRUKTORA NVARCHAR(20) NOT NULL,
	PREZIME_INSTRUKTORA NVARCHAR(20) NOT NULL,
	REGOZNAKA_VOZILA NVARCHAR(9) NOT NULL,
	CONSTRAINT PK_Instruktor PRIMARY KEY(SIFRA_INSTRUKTORA),
	CONSTRAINT FK_Instruktor FOREIGN KEY(REGOZNAKA_VOZILA) REFERENCES Vozilo(REGOZNAKA_VOZILA) 
	);

CREATE TABLE Polaznik(
	OIB_POLAZNIKA NUMERIC(11),
	IME_POLAZNIKA NVARCHAR(20) NOT NULL,
	PREZIME_POLAZNIKA NVARCHAR(20) NOT NULL,
	ADRESA NVARCHAR(40) NOT NULL,
	SIFRA_INSTRUKTORA NUMERIC(5),
	CONSTRAINT PK_Polaznik PRIMARY KEY(OIB_POLAZNIKA),
	CONSTRAINT FK_Polaznik FOREIGN KEY(SIFRA_INSTRUKTORA)REFERENCES Instruktor(SIFRA_INSTRUKTORA)
	);

CREATE TABLE Ispitivac(
	SIFRA_ISPITIVACA NUMERIC(5),
	IME_ISPITIVACA NVARCHAR(20) NOT NULL,
	PREZIME_ISPITIVACA NVARCHAR(20) NOT NULL,
	CONSTRAINT PK_Ispitivac PRIMARY KEY(SIFRA_ISPITIVACA)
	);

CREATE TABLE Ispit(
	SIFRA_ISPITA NUMERIC(7),
	OIB_POLAZNIKA NUMERIC(11)NOT NULL,
	SIFRA_ISPITIVACA NUMERIC(5)NOT NULL,
	DATUM_ISPITA DATETIME NOT NULL,
	ISHOD_ISPITA char(1) NOT NULL,
	PREZIME_ISPITIVACA NVARCHAR(20) NOT NULL,
	CONSTRAINT PK_Ispit PRIMARY KEY(SIFRA_ISPITA),
	CONSTRAINT FK_IspitPolaznik FOREIGN KEY(OIB_POLAZNIKA) REFERENCES Polaznik(OIB_POLAZNIKA),
	CONSTRAINT FK_IspitInstruktor FOREIGN KEY(SIFRA_ISPITIVACA) REFERENCES Ispitivac(SIFRA_ISPITIVACA)
	);