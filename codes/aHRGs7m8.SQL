	CREATE OR REPLACE FUNCTION ESERCIZIO_4
	(CODLAUREA 	IN VARCHAR2,
	 CODINS 	IN VARCHAR2)
	RETURIN VARCHAR2 IS
	
	CURSOR ULTIMO_ANNO IS
		SELECT  INSEGNAMENTO
		FROM 	PROP P
		WHERE   P.CODLAUREA = CODLAUREA;
	RIS ULTIMO_ANNO;
	RISULT VARCHAR2(1000);
	BEGIN
		OPEN ULTIMO_ANNO;
		LOOP
			EXIT WHEN ULTIMO_ANNO%NOTFOUND;
			FETCH ULTIMO_ANNO INTO RIS;
				RISULT := RISULT || RIS;
		END LOOP;
	END;
