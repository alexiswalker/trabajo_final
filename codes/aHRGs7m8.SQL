	CREATE OR REPLACE VIEW MEDIA_PRIMO AS
SELECT MATR,AVG(VOTO) MEDIA_P FROM ESAMI
  WHERE ANNO = 'I'
  GROUP BY MATR;

CREATE OR REPLACE VIEW MEDIA_SECONDO AS
SELECT MATR,AVG(VOTO) MEDIA_S FROM ESAMI
  WHERE ANNO = 'II'
  GROUP BY MATR;

CREATE OR REPLACE VIEW MEDIA_TERZO AS
SELECT MATR,AVG(VOTO) AS MEDIA_T FROM ESAMI
  WHERE ANNO = 'III'
  GROUP BY MATR;
  
CREATE OR REPLACE VIEW TOTALI AS 
SELECT M1.MATR, M1.MEDIA_P , M2.MEDIA_S, M3.MEDIA_T
  FROM MEDIA_PRIMO M1
    INNER JOIN MEDIA_SECONDO M2 ON M1.MATR = M2.MATR
    INNER JOIN MEDIA_TERZO M3 ON M1.MATR = M3.MATR;
  
CREATE OR REPLACE VIEW COPPIE AS
  SELECT A.MATR A_MATR, 
         B.MATR AS B_MATR
  FROM ISCRIZIONE A
  INNER JOIN ISCRIZIONE B ON A.CORSO = B.CORSO AND A.AA = B.AA
  INNER JOIN TOTALI T ON T.MATR = A.MATR
  INNER JOIN TOTALI T2 ON T2.MATR = B.MATR
  WHERE A.MATR<>B.MATR 
    AND T.MEDIA_P = T2.MEDIA_P AND T.MEDIA_S = T2.MEDIA_S
      AND T.MEDIA_T = T2.MEDIA_T;
 
